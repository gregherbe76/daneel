// ── STEP 0: Sourcing (optional) ───────────────────────────────────────────────

async function runSourcing(
  runId: number,
  jobId: number,
  job: { title: string; description: string; location: string; seniority: string; mustHaveSkills: string[] },
  insight: JobInsightResult,
  sourceTag: string,
  forceProvider?: import("./providers").AgentProvider,
): Promise<number[]> {
  await logStep(runId, "sourcing", "running", { jobTitle: job.title, sourceTag });

  const provider = forceProvider ?? (await resolveSourcingProvider()).provider;
  logger.info({ runId, step: "sourcing", provider: provider.name, sourceTag }, "Sourcing step dispatched");

  try {
    const rawSourcingResult = (await provider.run({
      step: "sourcing",
      runId,
      jobId,
      payload: { job, insight },
    })) as SourcingRunResult;

    // Providers may return either a bare array (legacy) or { candidates, stats }.
    // Normalize so the rest of the engine — and the sourcing log — can treat
    // both the same, while still surfacing GitHub-style filter counts.
    const candidates: SourcingCandidate[] = Array.isArray(rawSourcingResult)
      ? rawSourcingResult
      : rawSourcingResult.candidates;
    const sourcingStats: SourcingStats | null = Array.isArray(rawSourcingResult)
      ? null
      : rawSourcingResult.stats ?? null;

    // Deduplicate by githubUrl OR email OR linkedinUrl against existing
    // candidates. Any one signal is enough to identify the same person:
    // GitHub-sourced rows may have no email, Web Search (LinkedIn) results
    // have only a profile URL, and imported rows may have only an email.
    // Dedupe lookup ignores soft-deleted rows so a candidate the recruiter
    // trashed can be re-sourced (and re-applied to the job) instead of being
    // silently skipped against a tombstone in the trash bin.
    const existing = await db
      .select({
        email: candidatesTable.email,
        githubUrl: candidatesTable.githubUrl,
        linkedIn: candidatesTable.linkedIn,
      })
      .from(candidatesTable)
      .where(activeCandidateFilter);
    const existingEmails = new Set(
      existing.map((c) => c.email?.toLowerCase()).filter((v): v is string => !!v),
    );
    const existingGithub = new Set(
      existing.map((c) => c.githubUrl?.toLowerCase()).filter((v): v is string => !!v),
    );
    const existingLinkedIn = new Set(
      existing.map((c) => c.linkedIn?.toLowerCase()).filter((v): v is string => !!v),
    );

    const newCandidateIds: number[] = [];
    let skippedNoIdentity = 0;
    for (const c of candidates) {
      const emailKey = c.email?.toLowerCase() ?? null;
      const githubKey = c.githubUrl?.toLowerCase() || null;
      const linkedinKey = c.linkedinUrl?.toLowerCase() || null;
      // Require at least one durable identity signal — otherwise we cannot
      // dedupe on reruns and would create infinite duplicates of the same
      // generic-website hit.
      if (!emailKey && !githubKey && !linkedinKey) {
        logger.warn({ name: c.name }, "Sourcing skipped candidate with no identity signal");
        skippedNoIdentity++;
        continue;
      }
      if (githubKey && existingGithub.has(githubKey)) {
        logger.warn({ githubUrl: c.githubUrl }, "Sourcing skipped duplicate github URL");
        continue;
      }
      if (emailKey && existingEmails.has(emailKey)) {
        logger.warn({ email: c.email }, "Sourcing skipped duplicate email");
        continue;
      }
      if (linkedinKey && existingLinkedIn.has(linkedinKey)) {
        logger.warn({ linkedinUrl: c.linkedinUrl }, "Sourcing skipped duplicate linkedin URL");
        continue;
      }
      if (emailKey) existingEmails.add(emailKey);
      if (githubKey) existingGithub.add(githubKey);
      if (linkedinKey) existingLinkedIn.add(linkedinKey);

      // Validate the discovered email so recruiters don't waste outreach on
      // dead addresses. We use a lightweight MX-record lookup (no third-party
      // provider needed). The check never throws — on infra failure we mark
      // the address "unchecked" and let the recruiter decide.
      const emailToValidate = c.email || null;
      const validation = emailToValidate
        ? await validateEmail(emailToValidate)
        : { status: "invalid" as const, reason: "No email address" };

      const [inserted] = await db
        .insert(candidatesTable)
        .values({
          name: c.name,
          email: c.email || null,
          linkedIn: c.linkedinUrl || null,
          summary: c.summary || null,
          skills: c.skills,
          headline: c.headline || null,
          location: c.location || null,
          currentCompany: c.currentCompany || null,
          githubUrl: c.githubUrl || null,
          githubUsername: c.username || null,
          sourcingConfidence: c.confidence ?? null,
          source: sourceTag,
          emailSource: c.emailSource ?? null,
          emailValidationStatus: validation.status,
          emailValidationReason: validation.reason,
          emailValidatedAt: emailToValidate ? new Date() : null,
        })
        .returning({ id: candidatesTable.id });

      newCandidateIds.push(inserted.id);

      // Create application at "Sourced" stage
      await db.insert(applicationsTable).values({
        jobId,
        candidateId: inserted.id,
        stage: "Sourced",
        notes: `AI Generated — ${c.evidence}\nPotential risks: ${c.potentialRisks}`,
      });
    }

    await logStep(runId, "sourcing", "completed", { jobTitle: job.title, provider: provider.name, sourceTag }, {
      generated: candidates.length,
      saved: newCandidateIds.length,
      skippedNoIdentity,
      // Provider name + type are denormalized onto the log output so the
      // run-detail UI can label which provider (e.g. "Apify") produced the
      // numbers without having to re-fetch agent_provider rows or join input.
      providerName: provider.name,
      providerType: provider.type,
      // Persisted so the run-detail UI can explain why a "0 candidates" run
      // was actually filtered (empty bios / stale activity) vs. a broken search.
      stats: sourcingStats,
    });
    return newCandidateIds;
  } catch (err) {
    await logStep(runId, "sourcing", "failed", { jobTitle: job.title, provider: provider.name }, {
      providerName: provider.name,
      providerType: provider.type,
      error: err instanceof Error ? err.message : String(err),
    });
    throw err;
  }
}

// ── STEP 0b: Enrichment (optional) ───────────────────────────────────────────

/**
 * Returns true if a candidate needs enrichment — i.e. has sparse or placeholder data.
 * We only enrich candidates that are likely incomplete to avoid overwriting good data.
 */
function needsEnrichment(c: {
  source: string | null;
  summary: string | null;
  skills: string[];
  headline: string | null;
}): boolean {
  if (c.source === "LinkedIn Paste") return true;
  if (!c.summary || c.summary.startsWith("Imported from LinkedIn")) return true;
  if (c.skills.length === 0) return true;
  if (!c.headline) return true;
  return false;
}

async function runEnrichment(
  runId: number,
  jobId: number,
  job: { title: string; seniority: string; mustHaveSkills: string[] },
): Promise<void> {
  // Resolve provider — null means no provider is configured; enrichment is skipped
  const provider = await resolveEnrichmentProvider();
  if (!provider) {
    await logStep(runId, "enrichment", "failed", null, {
      note: "No enrichment provider is assigned. Configure one in Settings → Agent Providers → Workflow Step Assignments to enable enrichment.",
    });
    logger.info({ runId }, "Enrichment skipped: no provider configured");
    return;
  }

  // Only enrich candidates in this job's pipeline
  const jobApplications = await db
    .select({ candidateId: applicationsTable.candidateId })
    .from(applicationsTable)
    .where(eq(applicationsTable.jobId, jobId));

  if (jobApplications.length === 0) {
    logger.info({ runId }, "No candidates in pipeline — skipping enrichment");
    return;
  }

  const candidateIds = jobApplications.map((a) => a.candidateId);
  const allCandidates = await db
    .select()
    .from(candidatesTable)
    .where(and(inArray(candidatesTable.id, candidateIds), activeCandidateFilter));

  // Filter to only candidates with sparse / placeholder profiles
  const candidates = allCandidates.filter(needsEnrichment);

  if (candidates.length === 0) {
    logger.info({ runId }, "No candidates need enrichment — all profiles are already complete");
    await logStep(runId, "enrichment", "completed", { note: "All candidates already have complete profiles" }, {
      enriched: 0,
    });
    return;
  }

  await logStep(runId, "enrichment", "running", { candidateCount: candidates.length, provider: provider.name });
  logger.info({ runId, step: "enrichment", provider: provider.name, count: candidates.length }, "Enrichment step dispatched");

  const rawResponse = await provider.run({
    step: "enrichment",
    runId,
    jobId,
    payload: {
      candidates,
      jobContext: { title: job.title, seniority: job.seniority, mustHaveSkills: job.mustHaveSkills },
    },
  });

  logger.info(
    { runId, step: "enrichment", provider: provider.name, raw: JSON.stringify(rawResponse).slice(0, 500) },
    "Enrichment raw response",
  );

  const results = rawResponse as EnrichmentResult[];

  const now = new Date();
  let enrichedCount = 0;
  let partialCount = 0;
  let failedCount = 0;

  for (const r of results) {
    const confidence = typeof r.confidence === "number" ? Math.min(1, Math.max(0, r.confidence)) : 0;
    const missingSet = new Set<string>(Array.isArray(r.missingFields) ? r.missingFields : []);

    // Determine enrichment outcome based on confidence and missing fields
    const status: string =
      confidence >= 0.7 && missingSet.size === 0
        ? "enriched"
        : confidence >= 0.4
        ? "partial"
        : "failed";

    if (status === "enriched") enrichedCount++;
    else if (status === "partial") partialCount++;
    else failedCount++;

    // Only write fields that are actually present in the result and not listed as missing
    await db
      .update(candidatesTable)
      .set({
        ...(r.experienceSummary && !missingSet.has("summary") ? { summary: r.experienceSummary } : {}),
        ...(Array.isArray(r.skills) && r.skills.length > 0 && !missingSet.has("skills") ? { skills: r.skills } : {}),
        ...(r.enrichedHeadline && !missingSet.has("headline") ? { headline: r.enrichedHeadline } : {}),
        ...(r.currentCompany && !missingSet.has("currentCompany") ? { currentCompany: r.currentCompany } : {}),
        ...(r.location && !missingSet.has("location") ? { location: r.location } : {}),
        enrichedAt: now,
        enrichmentSource: provider.name,
        enrichmentConfidence: confidence,
        enrichmentStatus: status,
        updatedAt: now,
      })
      .where(eq(candidatesTable.id, r.candidateId));
  }

  logger.info(
    { runId, step: "enrichment", enrichedCount, partialCount, failedCount },
    "Enrichment normalized output",
  );

  await logStep(runId, "enrichment", "completed", { candidateCount: candidates.length, provider: provider.name }, {
    enriched: enrichedCount,
    partial: partialCount,
    failed: failedCount,
    avgConfidence: results.length > 0
      ? (results.reduce((s, r) => s + (typeof r.confidence === "number" ? r.confidence : 0), 0) / results.length).toFixed(2)
      : null,
  });
}

